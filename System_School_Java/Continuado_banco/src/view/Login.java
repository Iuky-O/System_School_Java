    //Função para acessar cadastrar estudante
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

           if(evt.getSource() == jButton3){
            String usuario = jTextField1.getText();
            String senha = new String(jPasswordField1.getPassword());
            
            ConnectBD bd = new ConnectBD();
            boolean resp = bd.autenticaLogin(usuario, senha);
            
            if(resp == true){
                
                JOptionPane.showMessageDialog(this,"Login bem sucedido!");
                new InterfaceGrafica().setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(this,"Usuario e senha invalidos!");
            }
            
            bd.desconnectBD();
          
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    //Função para acessar estudantes cadastrados
    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
  
        
           if(evt.getSource() == jLabel3){
               
            String usuario = jTextField1.getText();
            String senha = new String(jPasswordField1.getPassword());
            
            ConnectBD bd = new ConnectBD();
            boolean resp = bd.autenticaLogin(usuario, senha);
   
            if(resp == true){
                
                JOptionPane.showMessageDialog(this,"Login bem sucedido!");
                bd.selectCadastros();
                new exibirCad().setVisible(true);
               
                dispose();
            }else{
                JOptionPane.showMessageDialog(this,"Certifique-se de digitar usuário e senha na tela de login corretamente!");
            }
            
            bd.desconnectBD();
          
        }
        
    }//GEN-LAST:event_jLabel3MouseClicked

